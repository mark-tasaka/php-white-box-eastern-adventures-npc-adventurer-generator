function turn1HD ($class, $level)
{
    
    $turn = '';

    if($class != 'Sohei')
    {
        return $turn;
    }

    if($level === 1)
    {
        $turn = '11';
    }
    else if($level === 2)
    {
        $turn = '9';
    }
    else if($level === 3)
    {
        $turn = '7';
    }
    else if($level === 4)
    {
        $turn = '5';
    }
    else
    {
        $turn = 'D';
    }

return $turn;
}

